    private void impressionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_impressionActionPerformed
        // TODO add your handling code here:
        String date1 =((JTextField)datedime1.getDateEditor().getUiComponent()).getText();
        String date2 =((JTextField)datedime2.getDateEditor().getUiComponent()).getText();
        Map parameters = new HashMap();
	parameters.put("date1", date1);
	parameters.put("date2", date2); 
        try {    
        JasperDesign jdesign = JRXmlLoader.load("C:\\Users\\Kluce\\Documents\\NetBeansProjects\\Fimeco\\src\\report\\Montantprime.jrxml");
            JasperReport jreport = JasperCompileManager.compileReport(jdesign);
            JasperPrint jprint = JasperFillManager.fillReport(jreport, parameters, conn);
            JasperViewer jv = new JasperViewer(jprint, false);
            jv.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            jv.setVisible(true);
        } catch (JRException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }           
    }//GEN-LAST:event_impressionActionPerformed

