    private void rechercherpardatetotaldimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rechercherpardatetotaldimeActionPerformed
        
        String dateversement1 =((JTextField)datedime1.getDateEditor().getUiComponent()).getText();
        String dateversement2 =((JTextField)datedime2.getDateEditor().getUiComponent()).getText();
        
        String req5 ="select sum(montantHebdo) from dime "
                + "where dateVersement between '"+ dateversement1+"' and '"+ dateversement2+"'";
        try{  
            ps = conn.prepareStatement(req5);
            rs = ps.executeQuery();
            while(rs.next()){
            texteaff.setText(rs.getString(1));
             }        
        } catch (SQLException ex){
          JOptionPane.showMessageDialog(this, "Problème de connexion avec la base de données");
        }          
    }//GEN-LAST:event_rechercherpardatetotaldimeActionPerformed

