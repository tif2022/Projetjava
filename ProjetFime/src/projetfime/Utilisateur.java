    public void Deplace() {
        try {

            int row = tabutilisateur.getSelectedRow();
            this.test = (tabutilisateur.getModel().getValueAt(row, 0).toString());
            String req = " select * from  utilisateur where nom = '" + test + "'  ";
            ps = conn.prepareStatement(req);
            rs = ps.executeQuery();

            if (rs.next()) {
                String t1 = rs.getString("motpasse");
                txtmotpasse.setText(t1);
                String t2 = rs.getString("compte");
                txtcompte.setText(t2);
                String t3 = rs.getString("qualite");
                comboqualite.addItem(t3);
                
                String t4 = rs.getString("nom");
                txtnom.setText(t4);
                String t5 = rs.getString("prenoms");
                txtprenom.setText(t5);
            }
 
        } catch (Exception e) {
            System.out.println(e);
            
       }
        
     }
    
