    private void btnajoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnajoutActionPerformed

        String req="insert into utilisateur (motpasse,compte,qualite,nom,prenoms) values(?,?,?,?,?)";
        try{
            ps = conn.prepareStatement(req);
            ps.setString(1, txtmotpasse.getText());
            ps.setString(2, txtcompte.getText());
            ps.setString(3, (String) comboqualite.getSelectedItem());
            ps.setString(4, txtnom.getText());
            ps.setString(5, txtprenom.getText());
            ps.executeUpdate();
            // JOptionPane.showMessageDialog(this,"Insertion réussie!!!");
            txtmotpasse.setText("");
            txtcompte.setText("");
            comboqualite.setSelectedIndex(-1);
            txtnom.setText("");
            txtprenom.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(Utilisateur.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnajoutActionPerformed

    private void btnmodifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodifActionPerformed
        // TODO add your handling code here:   motpasse,compte,qualite,nom,prenoms
        DefaultTableModel d1 = (DefaultTableModel)tabutilisateur.getModel();
        int selectIndex = tabutilisateur.getSelectedRow();

        int utilid = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());
        String compte = txtcompte.getText();
        String motpasse  = txtmotpasse.getText();
        String qualite = (String) comboqualite.getSelectedItem();
        String nom = txtnom.getText();
        String prenoms = txtprenom.getText();
        try {    
        ps = conn.prepareStatement("update fidele set compte=?,motpasse=?,qualite=?,nom=?,prenoms=? where utilid=?");
        ps.setString(1, compte);
        ps.setString(2, motpasse);
        ps.setString(3, qualite);
        ps.setString(4, nom);
        ps.setString(5, prenoms);
        ps.setInt(6, utilid);  
        ps.executeUpdate();
//      JOptionPane.showMessageDialog(null,"Mise à jour effectuée!");
            afficherutilisateur();
            
            txtmotpasse.setText("");
            txtcompte.setText("");
            comboqualite.setSelectedIndex(-1);
            txtnom.setText("");
            txtprenom.setText("");
          
            txtmotpasse.requestFocus();
        } 
        catch (SQLException ex) {
            Logger.getLogger(Beneficiaire.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnmodifActionPerformed

