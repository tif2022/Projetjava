    private void rechercherparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rechercherparActionPerformed
        // Le code est ce qui suit   select fidele.numeroFidele as 'Matricule',codeDime as 'Code dîme',nom as 'Nom',"
        String dateversement1 =((JTextField)datea.getDateEditor().getUiComponent()).getText();
        String dateversement2 =((JTextField)dateb.getDateEditor().getUiComponent()).getText();
        
        String req5 ="select matricule as 'Matricule',codeDime as 'Code dîme',nom as 'Nom',"
                + "prenoms as 'Prénoms',contact as 'Contact',fonction as 'Fonction',sum(montantHebdo) as 'Total' "
                + "from fidele inner join dime "
                + "on fidele.numeroFidele = dime.numeroFidele "
                + "where dateVersement between '"+ dateversement1+"' and '"+ dateversement2+"' "
                + "group by matricule";
        try{  
            ps = conn.prepareStatement(req5);
            rs = ps.executeQuery();
            tabledimesfideles.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex){
          JOptionPane.showMessageDialog(this, "Problème de connexion avec la base de données");
        }
    }//GEN-LAST:event_rechercherparActionPerformed

