    private void impressionrapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_impressionrapActionPerformed

        String debutdate =((JTextField)datea.getDateEditor().getUiComponent()).getText();
        String findate =((JTextField)dateb.getDateEditor().getUiComponent()).getText();
        
       
        Map parameters = new HashMap();
	parameters.put("debutdate", debutdate);
	parameters.put("findate", findate);

        try {
       
        JasperDesign jdesign = JRXmlLoader.load("C:\\Users\\Kluce\\Documents\\NetBeansProjects\\Fimeco\\src\\report\\rapportmensuel.jrxml");
            JasperReport jreport = JasperCompileManager.compileReport(jdesign);

            JasperPrint jprint = JasperFillManager.fillReport(jreport, parameters, conn);
            JasperViewer jv = new JasperViewer(jprint, false);
            jv.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            jv.setVisible(true);
  
        } catch (JRException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_impressionrapActionPerformed

